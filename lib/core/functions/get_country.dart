  String strCountryCode = barcode.length > 2 ? barcode.substring(0, 3) : "000";
  int code = int.parse(strCountryCode);
  for (var range in iCodes) {
    if (range.start <= code && code <= range.end) {
      return range.country;
    }
  }
  return "Neznámý kód země";
}
